package test;

public class SwapTwoNum {

	public static void main(String[] args) {
	    
		int a=10,b=20;
		int temp=0;
		System.out.println("Before swapping");
		System.out.println("A :"+a+" B :"+b);
		
		temp=a;
		a=b;
		b=temp;
		System.out.println("After Swapping");
		System.out.println("A :"+a+" B :"+b);
		

	}

}
